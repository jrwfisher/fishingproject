package com.example.fishingproject.model;

public class Lure {
    private int lureId;
    private String lureName;
    private String lureType;

    public int getLureId() {
        return lureId;
    }

    public void setLureId(int lureId) {
        this.lureId = lureId;
    }

    public String getLureName() {
        return lureName;
    }

    public void setLureName(String lureName) {
        this.lureName = lureName;
    }

    public String getLureType() {
        return lureType;
    }

    public void setLureType(String lureType) {
        this.lureType = lureType;
    }
}
